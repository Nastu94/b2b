        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {SANCTUM_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Usa un Sanctum Personal Access Token e invialo come header: Authorization: Bearer <token>.</p>

        <h1 id="auth">Auth</h1>

    <p>Endpoint utili per verificare che il token Sanctum sia valido e che il chiamante
(es. bridge PrestaShop) stia autenticandosi correttamente.</p>

