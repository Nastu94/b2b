                                <h2 id="endpoints-GETapi-vendors-search">Cerca vendor con filtri categoria e posizione geografica.</h2>

<p>
</p>

<p>Supporta 2 modalità per filtro geografico:</p>
<ol>
<li>Coordinate dirette (latitude + longitude)</li>
<li>Indirizzo completo (address_line1, address_city, etc.) - Laravel fa geocoding automatico</li>
</ol>
<p>Se geocoding dell'indirizzo fallisce, continua senza filtro geografico.</p>

